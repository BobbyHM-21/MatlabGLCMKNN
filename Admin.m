% -GLCM
function pushbutton5_Callback(hObject, eventdata, handles)
    bw = handles.Img_gray;
%     glcm = graycomatrix(Img_gray); 
   glcm = graycomatrix(bw);
    stats = graycoprops(glcm,'Contrast Correlation Energy Homogeneity');
    Contrast = stats.Contrast ; 
    Correlation = stats.Correlation;
    Energy = stats.Energy;
    Homogeneity = stats.Homogeneity;
    
    ciri_uji = [Contrast, Correlation, Energy ,Homogeneity];
    
    set(handles.edit3,'String',num2str(Contrast));
    set(handles.edit4,'String',num2str(Correlation));
    set(handles.edit5,'String',num2str(Energy)); 
    set(handles.edit6,'String',num2str(Homogeneity)); 
    
    handles.ciri_uji = ciri_uji;
    guidata(hObject, handles)
    set(handles.pushbutton3,'Visible','on')

